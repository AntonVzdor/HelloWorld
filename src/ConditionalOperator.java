public class ConditionalOperator {

    public static void main(String[] args) {

        int money = 11;

        if (money > 10){
            System.out.println("pizza");
        } else if (money <= 10 && money >= 6) {
            System.out.println("hamburger");
        } else {
            System.out.println("sandwich");
        }
    }
    }

